function extendOrder(data) {
  const elbaph = data.fieldMapping.firstAppearance.order["Saga Finale"].Elbaph;
  const last = Number(elbaph[elbaph.length - 1].replace("Chapitre ", ""));
  for (let n = last + 1; n <= ORDER_END; n++) {
    elbaph.push(`Chapitre ${n}`);
  }
}

function buildEntry(id, def) {
  return {
    id,
    name: DISPLAY_NAMES[id],
    aliases: def.aliases ?? [],
    affiliation: def.affiliation,
    sub_affiliation: def.sub_affiliation ?? [],
    age: def.age,
    arc: "Elbaph",
    bounty: def.bounty,
    devilFruitType: def.devilFruitType,
    gender: def.gender,
    haki: def.haki,
    origin: def.origin,
    size: def.size,
    race: def.race,
    indice1: def.affiliation,
    indice2: def.indice2,
    indice3: def.indice3,
    firstAppearance: `Chapitre ${def.chapter}`,
  };
}

const data = JSON.parse(fs.readFileSync(MANGA_PATH, "utf8"));
const existing = new Set(data.characters.map((c) => c.id));

extendOrder(data);

let added = 0;
let updated = 0;

for (const [id, def] of Object.entries(NEW_CHARS)) {
  if (existing.has(id)) continue;
  data.characters.push(buildEntry(id, def));
  added++;
}

for (const c of data.characters) {
  if (UPDATE_ARC_IDS.has(c.id)) {
    c.arc = "Elbaph";
    updated++;
  }
  if (EXTRA_ALIASES[c.id]) {
    c.aliases = [...new Set([...(c.aliases ?? []), ...EXTRA_ALIASES[c.id]])];
  }
  if (c.id === "loki") {
    Object.assign(c, LOKI_DF_UPDATE);
  }
}

data.characters.sort((a, b) => a.name.localeCompare(b.name, "fr"));
fs.writeFileSync(MANGA_PATH, `${JSON.stringify(data, null, 2)}\n`, "utf8");
console.log(`+${added} persos, ${updated} arcs → Elbaph, order → Ch. ${ORDER_END}, total ${data.characters.length}`);
